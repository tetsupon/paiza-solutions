// 変数を使う

public class Main {
	public static void main(String[] args) {
	    String player = "勇者";                                //変数名をplayerにして"勇者"という文字列を代入
		System.out.println(player + "、荒野を歩いていた");         //変数名 + 文字列で表示させることができる
		System.out.println(player + "、モンスターと戦った");
		System.out.println(player + "、モンスターをたおした");
	}
}